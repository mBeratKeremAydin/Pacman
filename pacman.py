def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()


def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0


def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')
    
    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')


def move(lv,mode):
    """Move pacman and all ghosts."""
    global can
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for i in range(lv):
        point , course = ghosts[i]
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    if mode == 1:
        if state['score']==25:
            writer.undo()
            writer.write(state['score'])
            ontimer(show_win_screen,1000)
            return
        for i in range(lv):
            point , course = ghosts[i]
            if abs(pacman - point) < 20:
               ontimer(show_game_over_screen,1000)
               #ontimer(reset_game,1000)
               return
    elif mode == 2:
        for i in range(lv):
            point , course = ghosts[i]
            if abs(pacman - point) < 20:
                if can > 0:
                   can -=1
                   pacman.x= -40
                   pacman.y= -80
                else:
                    ontimer(show_game_over_screen,1000)
                    #ontimer(reset_game,1000)
                    return

    ontimer(lambda : move(lv,mode), 100)


def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

def set_level(lv):
    global level
    level = lv
    
def show_menu(mode):
    """Show the start menu and wait for user to press Enter."""
    global level
    clear()
    bgcolor('black')
    color('white')
    up()
    goto(0, 0)
    write('Press ENTER to Start', align='center', font=('Arial', 24, 'bold'))
    level = 0
    listen()
    onkey(lambda: start_game(4,mode), 'e')
    onkey(lambda: start_game(4,mode), 'E')
    onkey(lambda: start_game(5,mode), 'm')
    onkey(lambda: start_game(5,mode), 'M')
    onkey(lambda: start_game(6,mode), 'h')
    onkey(lambda: start_game(6,mode), 'H')

